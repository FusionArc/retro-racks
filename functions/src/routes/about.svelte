	<h4>Retro Racks</h4>
	<p>	We began design of the Retro Roof Rack based around a rack I once owned, which was originally manufactured by Volkswagen.</p>
	<!-- Image -->
	<p>VW cancelled the production their roof racks assumingly due to the low popularity of them. Evidently the racks were 	of poor quality in terms of materials and coatings, but the concept and style was clearly attractive.
	We loved the look of these original racks and took inspiration from them for our bespoke design.</p>
	<!-- Image -->
	<p>We felt we could improve on the construction, by upgrading the materials and enhance the visual appearance by giving it a retro feel.<br/>We gave the design an option of three different lengths, to ensure the product would be suitable for the various requirements of our customers.</p>
	<!-- Image -->
	<p>Customers are offered the choice of a hardwood deck for their Retro Roof Rack, which has proven to be increasingly popular and widley noticed for the superior quality and extensive functionality.</p>
	<p>As our products are bespoke and produced by hand it gives our customers the opportunity to purchase a truly unique and personalised item.</p>
	<!-- Image -->
	<p>Retro Racks are constructed by our dedicated in house fabricator/welder, every effort is made to ensure a high 	quality finish before they are given a cold galvanised primer, followed by a plastic powdercoat finish.
	The standard colour is satin black, although the final decision is given to customers at checkout. A vast array of <!-- link -->RAL colours are available.</p>
	<!-- Image -->
	<p> If hardwood slats are added to the rack, as standard they are planed all round, shaped and sanded, then treated with several coats of Danish oil, to ensure they are ready for the great British weather.</p>
	<!-- Image -->
	<p><em>We hope you love them as much as we love making them.</em></p>
